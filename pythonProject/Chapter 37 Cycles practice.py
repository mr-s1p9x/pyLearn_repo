### Задача 1 ###
# 1. Создайте функцию dict_to_list, которая будет конвертировать 
# словарь в список кортежей

# 2. Функция должна принимать словарь, а возвращать список кортежей, в каждом кортеже
# должны быть пары (key, value) из словаря

# 3. Если значение ключа - целое число, то его нужно умножить на 2
# перед добавлением в кортеж
myDict = {
    'id': 2345,
    'message': 'hello',
    'count': 2
}


def dict_to_list(dict):
    # Создаём временный список, куда будем собирать кортежи
    temp_list = []

    # Проходимся по парам ключ-значение в словаре
    for k, v in dict.items():
        # Если значение — целое число, умножаем его на 2
        if isinstance(v, int):
            v *= 2
        # Добавляем кортеж (ключ, значение) в список
        temp_list.append((k, v))
    # Возвращаем сформированный список кортежей
    return temp_list
         
# Вызываем функцию и сохраняем результат
res = dict_to_list(myDict)
print(res)

### Задача 2 ###
# 1. Создайте функцию filter_list, которая будет фильтровать список

# 2. У функции должно быть два параметра - список и тип значения

# 3. Функция должна вернуть новый список, в котором останутся только значения
# того типа, который был передан в вызове функции вторым аргументом

#[функция должна принимать список из разных типов, и вернуть результирующий список из 1 типов]
# 4. К примеру, мы вызываем функцию так:
# filter_list([35, True, 'abc', 10]) int) и получаем [35, 10]
# использовать нужно for in внутри функции
my_list = [35, True, 'abc', 10]

def filter_list(input_list, data_type):
    # Новый список для хранения отфильтрованных значений
    new_list = []
    # Проходимся по каждому элементу в исходном списке
    for item in input_list:
        # Если тип элемента соответствует переданному, и это не булево значение — добавляем
        if isinstance(item, data_type) and not isinstance(item, bool):
            new_list.append(item)
    # Возвращаем отфильтрованный список
    return new_list

# Вызываем функцию, фильтруем только целые числа (без True)
res_2 = filter_list(my_list, int)
print(res_2)